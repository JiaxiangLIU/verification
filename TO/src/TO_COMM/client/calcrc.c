/********** For CRC16 ************/

static unsigned short xcrc16(unsigned short crc, char cp)
{
	unsigned short t1 = 0, t2 = 0, t3 = 0, t4 = 0, t5 = 0, t6 = 0;
	t1 = (unsigned short) (crc >> 8);
	t2 = (unsigned short) (t1 & 0xff);
	t3 = (unsigned short) (cp & 0xff);
	t4 = (unsigned short) (crc << 8);
	t5 = (unsigned short) (t2 ^ t3);
	t6 = (unsigned short) (crc16_tab[t5] ^ t4);
	return t6;
}


unsigned short cal_crc16(unsigned short oldcrc16, void *buf, int len)
{
	unsigned short crc16 = oldcrc16;
	unsigned char *p = buf;
	int i;

	for (i = 0; i < len; i++) {
		crc16 = xcrc16(crc16, p[i]);
	}
	return crc16;
}


/********** For CRC32 ************/

unsigned int cal_crc32(unsigned int oldcrc32, void *buf, int len)
{
	unsigned char t;
	unsigned char *p = buf;

	while (len > 0) {
		t = (oldcrc32 ^ *p) & 0xff;
		oldcrc32 = (oldcrc32 >> 8) ^ crc_32_tab[t];
		len--;
		p++;
	}

	return oldcrc32;
}




